    fun sampleStats(count: IntArray): DoubleArray {
        var min = Int.MAX_VALUE
        var max = Int.MIN_VALUE
        var sum: Long = 0
        val mid: Double

        var size = 0
        var i = -1
        var j = count.size
        var balance = 0
        var modeI = Int.MAX_VALUE
        var modeV = Int.MAX_VALUE
        while (true) {
            // 结束条件
            if (i + 1 >= j || i >= j - 1) break
            //
            if (balance >= 0) {
                // 从右侧开始计算
                j--
                // max
                if (max == Int.MIN_VALUE && count[j] != 0) max = j
                // sum
                sum += count[j] * j
                // size
                size += count[j]
                // mid
                balance -= count[j]
                // mode
                if (count[j] > modeV || modeV == Int.MAX_VALUE) {
                    modeV = count[j]
                    modeI = j
                }
            } else {
                // 从左侧开始计算
                i++
                // min
                if (min == Int.MAX_VALUE && count[i] != 0) min = i
                // sum
                sum += count[i] * i
                // size
                size += count[i]
                // mid
                balance += count[i]
                // mode
                if (count[i] > modeV || modeV == Int.MAX_VALUE) {
                    modeV = count[i]
                    modeI = i
                }
            }
        }
        mid = when (balance) {
            in Int.MIN_VALUE until 0 -> j * 1.0
            in 1..Int.MAX_VALUE -> i * 1.0
            else -> (i + j) * 1.0 / 2
        }
        return doubleArrayOf(min * 1.0, max * 1.0, sum * 1.0 / size, mid, modeI * 1.0)
    }

