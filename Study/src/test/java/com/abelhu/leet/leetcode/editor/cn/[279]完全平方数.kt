class Solution279 {
    fun numSquares(n: Int): Int {
        val dp = IntArray(n + 1) { n }
        dp[0] = 0
        for (i in 0..n) {
            var j = 1
            while (i - j * j >= 0) {
                dp[i] = kotlin.math.min(dp[i], 1 + dp[i - j * j])
                j++
            }
        }
        return dp[n]
    }

    @Test
    fun test0() {
        assert(3 == numSquares(12))
    }

    @Test
    fun test1() {
        assert(2 == numSquares(13))
    }

    @Test
    fun test2() {
        assert(3 == numSquares(14))
    }
}
